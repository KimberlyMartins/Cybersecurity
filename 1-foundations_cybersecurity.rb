                     SEGURANÇA CIBERNÉTICA 
A prática de garantir a confidencialidade, a integridade e a disponibilidade das informações, protegendo redes, dispositivos, pessoas e dados contra o acesso não autorizado ou a exploração criminosa

                     SEGURANÇA NA NUVEM
O processamento para garantir que os recursos armazenados na Nuvem sejam configurados adequadamente e que o acesso a esses recursos seja limitado a usuários autorizados

                     SEGURANÇA DE REDE
A prática de manter a infraestrutura de rede de uma organização protegida contra acesso não autorizado. Envolve manter a infraestrutura de comunicação de uma organização protegida contra invasões, utilizando ferramentas como firewalls, criptografia e VPNs.

                     POSTURA DE SEGURANÇA 
A capacidade de uma organização de gerenciar sua defesa de recursos e dados críticos e reagir a mudanças. A Representa o nível de preparo de uma organização para identificar vulnerabilidades, responder a incidentes e manter defesas atualizadas. 

                           AMEAÇA
Uma ameaça é qualquer evento que possa comprometer dados ou sistemas, podendo ser interna — como funcionários ou parceiros com acesso privilegiado — ou externa, representada por atores maliciosos como hackers ou grupos criminosos.
Ameaça interna: Um funcionário ou ex-funcionário, um fornecedor externo ou um parceiro confiável que represente um Risco à segurança.
Ator da ameaça: Qualquer pessoa ou grupo que apresente um Risco à segurança.

             INFORMAÇÕES DE IDENTIFICAÇÃO PESSOAL (PII) 
Qualquer informação usada para inferir a identidade de um indivíduo. São dados que permitem reconhecer um indivíduo, como nome, CPF ou endereço

        INFORMAÇÕES SENSÍVEIS DE IDENTIFICAÇÃO PESSOAL (SPII) 
Um tipo específico de PII que se enquadra em diretrizes de manuseio mais rigorosas. São um subconjunto de PII que exigem maior proteção, como dados bancários, biometria ou histórico médico.

                     HABILIDADES TÉCNICAS 
Habilidades que exigem conhecimento de ferramentas, procedimentos e políticas específicas. Como programação ou administração de redes. 

                     HABILIDADES TRANSFERÍVEIS 
Habilidades de outras áreas que podem ser aplicadas a diferentes carreiras. Como comunicação, liderança e resolução de problemas, que fortalecem a atuação em qualquer carreira.



 