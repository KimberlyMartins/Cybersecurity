                     SEGURANÇA CIBERNÉTICA 
A prática de garantir a confidencialidade, a integridade e a disponibilidade das informações, protegendo redes, dispositivos, pessoas e dados contra o acesso não autorizado ou a exploração criminosa

                     SEGURANÇA NA NUVEM
O processamento para garantir que os recursos armazenados na Nuvem sejam configurados adequadamente e que o acesso a esses recursos seja limitado a usuários autorizados

                     SEGURANÇA DE REDE
A prática de manter a infraestrutura de rede de uma organização protegida contra acesso não autorizado

                     POSTURA DE SEGURANÇA 
A capacidade de uma organização de gerenciar sua defesa de recursos e dados críticos e reagir a mudanças 

                           AMEAÇA
Qualquer circunstância ou evento que possa afetar negativamente os recursos.
Ameaça interna: Um funcionário ou ex-funcionário, um fornecedor externo ou um parceiro confiável que represente um Risco à segurança.
Ator da ameaça: Qualquer pessoa ou grupo que apresente um Risco à segurança.

             INFORMAÇÕES DE IDENTIFICAÇÃO PESSOAL (PII) 
Qualquer informação usada para inferir a identidade de um indivíduo.

        INFORMAÇÕES SENSÍVEIS DE IDENTIFICAÇÃO PESSOAL (SPII) 
Um tipo específico de PII que se enquadra em diretrizes de manuseio mais rigorosas.

                     HABILIDADES TÉCNICAS 
Habilidades que exigem conhecimento de ferramentas, procedimentos e políticas específicas.

                     HABILIDADES TRANSFERÍVEIS 
Habilidades de outras áreas que podem ser aplicadas a diferentes carreiras.



 